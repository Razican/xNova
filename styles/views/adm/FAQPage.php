	<a name="SEARCH"></a>
	<td class="c">Funcionamiento del buscador avanzado</td>
	<td class="c" width="2%"><a href="#UP" title="Subir"><img src="../styles/images/Adm/UP.png" width="16" height="16"></a></td>
</tr><tr>
	<th colspan="2">
		<b>El buscador avanzado es capaz de buscar información parcial de un usuario especificado.</b>	<br><br>

		<b>Tipos de búsqueda</b>	<br>
		.- <b>Búsqueda normal:</b> <i>busca usuarios con la palabra clave escrita, pero ésta buscara cualquier coincidencia.</i>	<br>
		.- <b>Búsqueda exacta:</b> <i>buscara exactamente como está escrita la palabra clave.</i>	<br>
		.- <b>Que empieze con "X" palabra o letra;</b> <i>Ejemplo: Escribis palabra clave "a" y buscara todos los nombres que empiezen con esa letra.</i>	<br>
		.- <b>Que termine con "X" palabra o letra:</b> <i>es lo mismo que el punto anterior, pero a la inversa.</i>	<br><br>

		<b>Tipos de orden</b>	<br>
		.- <b>Ascendiente:</b> <i>ordena de menor a mayor.</i>	<br>
		.- <b>Descendiente:</b> <i>ordena de mayor a menor.</i>	<br><br>

		Tambien pueden ordenarse por ID, Nombre, etc. (Según la búsqueda).	<br>
		Si la palabra clave esta en blanco (vacío), buscara todos los nombres que haya en la tabla.
	</th>
</tr>
