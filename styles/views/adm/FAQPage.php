	<a name="BAN"></a>
	<td class="c">Funcionamiento del panel de suspensión</td>
	<td class="c" width="2%"><a href="#UP" title="Subir"><img src="../styles/images/Adm/UP.png" width="16" height="16"></a></td>
</tr><tr>
	<th colspan="2">
		<b>En caso de que...</b>	<br>
		.- <b>El usuario no está suspendido:</b> <i>Puedes suspenderlo agregando (si queres) fecha de autoreanudación (deja de estar suspendido), razón, modo vacaciones, etc.</i>	<br>
		.- <b>El usuario está suspendido:</b> <i>Puedes editarlo completamente sin necesidad de sacarle la suspensión o reiniciar los datos. Para restarle dias, horas, minutos o segundos de suspensión puedes simplemente poner un "-5" (Es un ejemplo) y se restara esa cantidad de tiempo. También puedes editarle la razón y activar o desactivar el modo vacaciones.</i>	<br><br>

		<b>Yo quiero suspender a un usuario, pero no aparece en la lista, ¿Qué sucede?</b> <br>
		No debes tener los permisos suficientes para suspender a tal usuario, por ejemplo, un moderador u operador no puede suspender a un usuario con rango mayor, como el de un administrador.<br><br>
		<b>Suspendí a un usuario y no aparece en la lista para sacar la suspensión, ¿Qué sucede?</b> <br>
		Si usaste otro sistema de suspensión o lo hiciste manualmente desde la base de datos es posible que no aparezca en la lista ya que hiciste una mala suspensión. En ese caso lo más sencillo seria hacer lo siguiente: Suspender al usuario con el sistema de suspensión de xNova y después sacarle la suspensión con el mismo.
	</th>
</tr>
